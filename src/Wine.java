public class Wine {
    private double f_acid;
    private double v_acid;
    private double c_acid;
    private double r_sugar;
    private double chlorid;
    private double f_sulf;
    private double t_sulf;
    private double dens;
    private double pH;
    private double sulph;
    private double alc;
    private double qual;

    private int ID;
    private String WineKind;


    public Wine() {
    }

    public double getF_acid() {
        return f_acid;
    }

    public void setF_acid(double f_acid) {
        this.f_acid = f_acid;
    }

    public double getV_acid() {
        return v_acid;
    }

    public void setV_acid(double v_acid) {
        this.v_acid = v_acid;
    }

    public double getC_acid() {
        return c_acid;
    }

    public void setC_acid(double c_acid) {
        this.c_acid = c_acid;
    }

    public double getR_sugar() {
        return r_sugar;
    }

    public void setR_sugar(double r_sugar) {
        this.r_sugar = r_sugar;
    }

    public double getChlorid() {
        return chlorid;
    }

    public void setChlorid(double chlorid) {
        this.chlorid = chlorid;
    }

    public double getF_sulf() {
        return f_sulf;
    }

    public void setF_sulf(double f_sulf) {
        this.f_sulf = f_sulf;
    }

    public double getT_sulf() {
        return t_sulf;
    }

    public void setT_sulf(double t_sulf) {
        this.t_sulf = t_sulf;
    }

    public double getDens() {
        return dens;
    }

    public void setDens(double dens) {
        this.dens = dens;
    }

    public double getpH() {
        return pH;
    }

    public void setpH(double pH) {
        this.pH = pH;
    }

    public double getSulph() {
        return sulph;
    }

    public void setSulph(double sulph) {
        this.sulph = sulph;
    }

    public double getAlc() {
        return alc;
    }

    public void setAlc(double alc) {
        this.alc = alc;
    }

    public double getQual() {
        return qual;
    }

    public void setQual(double qual) {
        this.qual = qual;
    }

    public int getID() {
        return ID;
    }
    public void setID(int ID) {
        this.ID = ID;
    }


    public String getWineKind() {
        return WineKind;
    }
    public void setWineKind(String wineKind) {
        WineKind = wineKind;
    }


}
